# *** Alguns métodos que podem ser utilizados com Strings ***

#----- VARIÁVEIS -----
nome = "vitor"
#----- VARIÁVEIS -----

# ----- MÉTODOS -----
print (len (nome)              )  # LEN serve para retornar o número de caracteres.
print (nome.find ("v")         )  # FIND serve para localizar a posição de um caractere.
print (nome.capitalize ()      )  # Capitalize serve para deixar primeiro caracter em maiúsculo.
print (nome.upper ()           )  # Upper serve para deixar todos caracteres em maiúsculo.
print (nome.lower ()           )  # Lower serve para deixar todos caracteres em minúsculo.
print (nome.isdigit ()         )  # Isdigit verifica se os caracteres são números ou não.
print (nome.isalpha ()         )  # Isalpha verefica se os caracteres são letras ou não.
print (nome.count ("i")        )  # Count retorna a quantidade de caracteres um caracter especifíco tem.
print (nome.replace ("v", "t") )  # Replace substitui um caracter por outro.
print (nome*8                  )  # * repete o texto em determinadas vezes.
