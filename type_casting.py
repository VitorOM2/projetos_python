# *** Converte um tipo de dado para outro ***

# ----- VARIÁVEIS -----
x = 1    #int
y = 2.0  #float
z = "3"  #str
# ----- VARIÁVEIS -----

# *Sem converter*
print (x)
print (y)
print (z)
# *Sem converter*

print (" ")

# *Convertendo*
x = str   (x)
y = int   (y)
z = float (z)

print ("X é: " + x ) #str
print (y           ) #int
print (z           ) #float
# *Convertendo*