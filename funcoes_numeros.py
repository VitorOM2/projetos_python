# *** Funções de matemática ***

# ----- BIBLIOTECAS -----
import math
# ----- BIBLIOTECAS -----

# ----- VARIÁVEIS -----
pi = 3.14
x  = 1
y  = 2
z  = 3
# ----- VARIÁVEIS -----

print (math.ceil  (pi)      ) # CEIL  = arredonda o número para cima.
print (math.floor (pi)      ) # FLOOR = arredonda o número para baixo.
print (abs        (pi)      ) # ABS   = retorna o valor absoluto.
print (pow        (pi, 2)   ) # POW   = exponencia um valor por outro.
print (math.sqrt  (pi)      ) # SQRT  = retorna a raiz quadrada.
print (max        (x, y, z) ) # MAX   = retorna o maior valor.
print (min        (x, y, z) ) # MIN   = retorna o menor valor.